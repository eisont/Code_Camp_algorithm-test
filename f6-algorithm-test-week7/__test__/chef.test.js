const chef = require("../src/chef");

describe("나는 요리사다", () => {
  test("점수의 합이 19여야 합니다", () => {
    expect(
      chef([
        [5, 4, 4, 5],
        [5, 4, 4, 4],
        [5, 5, 4, 4],
        [5, 5, 5, 4],
        [4, 4, 4, 5],
      ])
    ).toEqual(19);
  });

  test("점수의 합이 17이어야 합니다", () => {
    expect(
      chef([
        [4, 4, 3, 3],
        [5, 4, 3, 5],
        [5, 5, 2, 4],
        [5, 5, 5, 1],
        [4, 4, 4, 4],
      ])
    ).toEqual(17);
  });
});

const receipt = require("../src/receipt");

describe("영수증", () => {
  test("10과 [1, 1, 1, 1, 1, 1, 1, 1, 1]을 입력받은 경우 1을 리턴해야 합니다", () => {
    expect(receipt(10, [1, 1, 1, 1, 1, 1, 1, 1, 1])).toEqual(1);
  });

  test("9850과 [1050, 800, 420, 380, 600, 820, 2400, 1800, 980]을 입력받은 경우 600을 리턴해야 합니다", () => {
    expect(
      receipt(9850, [1050, 800, 420, 380, 600, 820, 2400, 1800, 980])
    ).toEqual(600);
  });
});
